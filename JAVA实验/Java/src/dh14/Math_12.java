package dh14;
/*Math类的使用方法
 */
public class Math_12 {
	public static void main(String[] args) {
		//输出成员变量
		System.out.println("E="+Math.E);
		System.out.println("PI="+Math.PI);
		//输出绝对值
		System.out.println("绝对值"+Math.abs(-10));
		//输出向上取整
		System.out.println("向上取整"+Math.ceil(12.5));
		//输出向下取整
		System.out.println("向下取整"+Math.floor(12.46));
		//输出随机数
		System.out.println("0.0-1.0内的随机数"+Math.random());
		//输出正平方根
		System.out.println("平方根="+Math.sqrt(4.0));
		//输出a的b次幂
		System.out.println("a的b次幂"+Math.pow(2, 10));
		//输出最接近参数的int值
		System.out.println("最接近参数的int值"+Math.round(8.02));
		
		
	}

}
